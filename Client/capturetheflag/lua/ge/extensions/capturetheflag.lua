local function dropFlag(dropCoords, isLocal, pickupTimeout)
	if flagCarrier then
		if isLocal then
			log("I","CtF", "DropFlagLocal "..tostring(flagCarrierID))
			--TriggerServerEvent("CtFflagDropped", tostring( MPVehicleGE.getServerVehicleID(flagCarrier:getID())))
