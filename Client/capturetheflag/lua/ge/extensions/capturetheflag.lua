		x = vehiclePosition.x,
		y = vehiclePosition.y,
		z = vehiclePosition.z
		}

	local strpos = jsonEncode(postable)
	strpos = strpos:gsub(":", ";")
	print(strpos)
	TriggerServerEvent("CtFsetFlagSpawnTo", strpos)
end

